			<div id="trainerinfo"><p style="clear: both;">Welcome to <i>Trainer</i>, the section of this website that cites my history with this game and all the precious Pok&eacute;mon I've caught and raised over the years. Since <ruby>Ruby</ruby> was the first real Pok&eacute;mon game I owned, I like to say that I'm a Hoenn kid at heart.</p>
	<p><img src="ruby.png" class="rubyimg" height="80">I've been into Pok&eacute;mon since 1998, when the anime first came out &mdash; I had a jigglypuff card that I was particularly attached to. But I didn't actually own a game until August 2003 after watching my camp friends play it all summer. I really didn't know anything about the gameplay, but I was still so enthusiastic about it that some of my classmates gave me the nickname "Pok&eacute;mon Master" anyway. Which I still am, by the way. My obsession for other things will come and go, but Pok&eacute;mon will always own at least 50% of my heart.</p>
	<p>Since then, I've played and owned at least one game in every generation that's come out. Though I'm a Hoenn kid, I do consider Gen V to be the best both gameplay and story-wise; and I remain relentlessly optimistic for the games to come, especially as Legends Arceus is the most fun I've ever had playing Pok&eacute;mon, period.</p>
	<p>I'm neither outdoorsy nor a serious animal-lover IRL (I do have a dog though), but I'd like to believe I would be a little more in the Pok&eacute;mon world and I do care about protecting those in need, hence being a Ranger. The type of Pok&eacute;mon I like fit as well - I've always been drawn to grass starters. Most of all, I remember the first time I saw the sprite for a female Pok&eacute;mon Ranger in Ruby, and I'd never connected to an NPC class more ever since, much less based purely on aesthetic.</p>
	<p>Overall, I'm a casually enthusiastic collector with mildly competitive knowledge but not enough interest in it. In the end, my main enjoyment with Pok&eacute;mon is loving them.</p>

	<hr />

	<blockquote><img src="https://icirr.us/trainer/trainer.gif" class="trainerimg"><p>Rangers appearing in core series games collect and train Pokémon just like other regular Pokémon Trainers. However, they are still primarily concerned with protecting wild Pokémon, keeping the environment safe, and making sure travelers do not get injured or lost when walking through the wilderness, as they mention in their battle dialogue.</p>

<p>They specialize in <grass>Grass-type Pokémon</grass> in Generation <ruby>I</ruby><sapphire>I</sapphire><emerald>I</emerald> and they often have healing items. In Generation <dpp>IV</dpp> onward, they use a variety of powerful Pokémon, similar to Ace Trainers.</p>

<p>In <bw2>Pokémon Black and White and Black 2 and White 2</bw2>, they give out Berries when defeated, unless if they are encountered in the Big Stadium or Small Court, where they instead give out Max Repels. <small><i>- Bulbapedia</i></p></small> </blockquote>

<hr />

<p><img src="https://icirr.us/trainer/kingdra.gif" class="kingdra favmon"> <b>kingdra</b> <img src="https://icirr.us/mb.png"> My favorite &hearts; I like 'mon based on their aesthetic and typing most of the time, though their battle viability may make a difference in some cases. Kingdra is the peak of this for me; as a huge water lover and a dragon lover and the excellent defense of those two types together, as well as kingdra'ss simplistic-but-elegant design, it's no wonder it would become my favorite. It was difficult to get in gen III, though, but my brother and I had a link cable and I managed to evolve one, named <i>Dumpling</i>. It had Sniper, which is its inferior ability, and middling stats &mdash; but as someone who likes long range attacks, it works for me. Though the others here may come quite close, I don't think anything will top a pok&eacute;mon that feels made for me.</p>

<p><img src="https://icirr.us/trainer/sceptile-mega.gif" class="sceptile favmon"><b>sceptile</b> <img src="https://icirr.us/mb.png"> My first ever pok&eacute;mon &hearts; I mostly chose treecko because the friend from summer camp who I'd watched all the time had a treecko (also named Ecko, by the way &mdash; I am so original) who she evolved into a grovyle, and I thought both were pretty cool. Ever since then, whenever I played a Hoenn game, it didn't feel right if I didn't start with treecko first. Sceptile is awesome too; I love me a glass cannon, because that's the kind of fighter I am myself. Mega sceptile being half dragon type (numerically technically my 'favorite' type) felt perfect for it too. And for me.</p>

<p><img src="https://icirr.us/trainer/talonflame.gif" class="talonflame favmon"><b>talonflame</b> <img src="https://icirr.us/mb.png"> I've always been a huge early birds fan, and though talonflame wasn't the first for me, the moment I saw it I fell in love with its design. I love a red and blue color palette and that's what talonflame is, with the fire and flying type on top of that, a fun typing even if it is especially weak to rocks. But who cares about rocks when you've got a badass bird with one of the best speed stats, you know? Its name also sounds like the old nicknames I used to give my pok&eacute;mon (as you may see here), so it just fits alongside me.</p>

<p><img src="https://icirr.us/trainer/mienshao.gif" class="mienshao favmon"><b>mienshao</b> <img src="https://icirr.us/mb.png"> Absolutely my favorite pok&eacute;mon in gen V <strike>rip my love for bisharp tbh</strike>, and also my favorite fighting type, which I've always liked in theory but much more rarely in practice. Mienshao, however, fits the bill perfectly for me &mdash; an elegant long-range fighter with the drawback of being frail if it gets hit more than once. Sounds familiar, doesn't it? Not to mention its lovely color palette, because honestly I liked it the moment I saw it before I even knew how it played. I love pastels, and you can never go wrong with pinks. I don't feel any particular way about ermines, but maybe I should.</p>

<p><img src="https://icirr.us/trainer/luxray.gif" class="luxray favmon"><b>luxray</b> <img src="https://icirr.us/mb.png"> Much like all of the others here, I knew I loved luxray the moment I saw it. I hadn't been impressed by its pre-evolutions much for the first time when I was 14 &mdash; I feel differently now, because shinx is much cuter in Legends Arceus &mdash; but my opinion changed the moment my luxio evolved into luxray. I'm an edgy bitch who likes edgy badass pok&eacute;mon, and though luxray isn't exactly remarkably out there, that's precisely why I like it the way it is. I love the blue and yellow as well, and the dark blue is what makes it my favorite electric-type design, getting one over another electric dog (manectric.)</p>

<p><img src="https://icirr.us/trainer/aggron.gif" class="aggron favmon"><b>aggron</b> <img src="https://icirr.us/mb.png"> I liked aron the first time I saw it in Dewford cave, felt ambivalent about lairon, and then popped off when I saw aggron. My love for aggron was the first time I loved steel types, which has become my main 'type' today. Though aggron isn't particularly elegant, it's also not <em>not</em> elegant. Paired with the edgy badassness that I like, and with having such a cute first evolution, the feelings I had for aggron in 2004 are the same ones that I have even today. Lairon <em>is</em> cool, but in the sense that they really did succeed at making an awkward teenager mid-evolution out of it. </p>

